package com.firebird.character;

public class character {
    public int HP_C;        //체력
    public int AD_C;        //공격력
    public int DF_C;        //방어력
    public int LV_C;        //레벨



    public character(int HP_C, int AD_C, int DF_C, int LV_C) {
        this.HP_C = HP_C;
        this.AD_C = AD_C;
        this.DF_C = DF_C;
        this.LV_C = LV_C;
    }
}


